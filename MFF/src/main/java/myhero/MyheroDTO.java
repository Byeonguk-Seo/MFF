package myhero;

public class MyheroDTO {
	private int num;
	private String uid;
	private String hero;
	private int hlevel;
	private int exp;
	private int attack;
	private int pdefense;
	private int edefense;
	private int life;
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public String getUid() {
		return uid;
	}
	public void setUid(String uid) {
		this.uid = uid;
	}
	public String getHero() {
		return hero;
	}
	public void setHero(String hero) {
		this.hero = hero;
	}
	public int getHlevel() {
		return hlevel;
	}
	public void setHlevel(int hlevel) {
		this.hlevel = hlevel;
	}
	public int getExp() {
		return exp;
	}
	public void setExp(int exp) {
		this.exp = exp;
	}
	public int getAttack() {
		return attack;
	}
	public void setAttack(int attack) {
		this.attack = attack;
	}
	public int getPdefense() {
		return pdefense;
	}
	public void setPdefense(int pdefense) {
		this.pdefense = pdefense;
	}
	public int getEdefense() {
		return edefense;
	}
	public void setEdefense(int edefense) {
		this.edefense = edefense;
	}
	public int getLife() {
		return life;
	}
	public void setLife(int life) {
		this.life = life;
	}
	
	
}
