package hero;

public class HeroDTO {
	private int num;
	private String hero;
	private String upday;
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public String getHero() {
		return hero;
	}
	public void setHero(String hero) {
		this.hero = hero;
	}
	public String getUpday() {
		return upday;
	}
	public void setUpday(String upday) {
		this.upday = upday;
	}
	
	
}
